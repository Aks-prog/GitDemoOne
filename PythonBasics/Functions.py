# In Python, Function is a group of related statements that perform a specific task.

# Function Declaration.
def Greet():
    print("Good Morning")

# This is Parameterized function.
def name(name):
    print("Hello "+name)

# Function for sum of two integers.
def addIntegers(a, b):
    print(a+b)

# Function for sum of two numbers uisng 'return' keyword.
def sumIntegers(c, d):
    return c + d

# Calling all functions.
Greet()
name("Ankit Kumar Singh")
addIntegers(2, 3)
print(sumIntegers(5, 4))
